const $ = (s, c) => (c || document).querySelector(s);
const $$ = (s, c) => (c || document).querySelectorAll(s);

function debounce(fn, ms) {
    let t;
    return function() {
        const a = arguments, c = this;
        clearTimeout(t);
        t = setTimeout(() => fn.apply(c, a), ms);
    };
}

function setLanguage(lang, animate) {
    if (!translations[lang]) return;
    currentLang = lang;
    localStorage.setItem('appLang', lang);

    const langBtns = $$('.lang-btn');
    for (let i = 0; i < langBtns.length; i++) {
        const btn = langBtns[i];
        const isActive = btn.getAttribute('data-lang') === lang;
        btn.className = 'lang-btn w-full text-left px-3 py-2 text-[13px] rounded-md transition-colors ' +
            (isActive ? 'text-white bg-[#111]' : 'text-[#EDEDED] hover:bg-[#111] hover:text-white');
    }

    const doUpdate = () => {
        const indicator = $('#current-lang-indicator');
        if (indicator) indicator.textContent = lang.toUpperCase();

        const els = $$('[data-i18n]');
        const t = translations[lang];
        for (let i = 0; i < els.length; i++) {
            const el = els[i];
            const key = el.getAttribute('data-i18n');
            const val = t[key];
            if (!val) continue;
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.placeholder = val;
            } else {
                const icon = el.querySelector('i');
                if (icon) {
                    el.textContent = '';
                    el.appendChild(icon);
                    el.append(' ' + val);
                } else {
                    el.textContent = val;
                }
            }
        }

        const dropdowns = $$('.custom-dropdown');
        for (let i = 0; i < dropdowns.length; i++) {
            const active = $('.dropdown-item.active', dropdowns[i]);
            const sel = $('.dropdown-selected', dropdowns[i]);
            if (active && sel) sel.textContent = active.textContent;
        }
    };

    if (animate) {
        const targets = $$('[data-i18n], .dropdown-selected, #current-lang-indicator');
        for (let i = 0; i < targets.length; i++) {
            targets[i].style.cssText = 'transition:opacity .15s;opacity:0';
        }
        setTimeout(() => {
            doUpdate();
            for (let i = 0; i < targets.length; i++) {
                targets[i].style.opacity = '1';
            }
            setTimeout(() => {
                for (let i = 0; i < targets.length; i++) {
                    targets[i].style.cssText = '';
                }
            }, 150);
        }, 150);
    } else {
        doUpdate();
    }
}

