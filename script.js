    const t = translations[currentLang];
    // Берем перевод из словаря или используем дефолтные значения
    const prefix = isFiltered ? (t.counter_found || "Found: ") : (t.counter_total || "Total: ");
    
    counterEl.textContent = prefix + count;
    
